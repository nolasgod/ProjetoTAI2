// local import of the exported AngularPage class
import {AngularLoginPage} from './angularPage';

// The jasmine typings are brought in via DefinitelyTyped ambient typings.
describe('angularjs LoginPage', () => {
  it('should greet the named user', () => {
    let angularLoginPage = new AngularLoginPage();
    angularLoginPage.get();
    angularLoginPage.setName('Julie');
    expect(angularLoginPage.getGreeting()).toEqual('Hello Julie!');
  });
});
